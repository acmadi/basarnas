<div id='judul'>PENGGUNA LOGIN</div>
<p>Pengguna Login adalah pengguna yang dapat mengakses ke sistem, pengaksesan terhadap menu yang ada tergantung hak akses yang diberikan oleh Administrator Sistem </p>
<p>Halaman ini diperuntukkan untuk melakukan Manajemen Pengguna Login terhadap Sistem ini.</p>
<p>Berikut ini langkah dalam Manajemen Pengguna Login :</p>

<div id='judul_1'>Tambah Pengguna</div>
<ol class='ds'>
<li>Klik Tambah</li>
<li>Isi semua Kolom isian yang ada</li>
<li>Jika semua kolom telah terisi, Klik Simpan untuk menyimpan Pengguna Login yang baru</li>
</ol>

<div id='judul_1'>Ubah Pengguna</div>
<ol class='ds'>
<li>Pilih salah satu Pengguna yang ingin Anda Ubah</li>
<li>Klik Ubah</li>
<li>Lakukan perubahan pada kolom perubahan yang ada</li>
<li>Jika semua kolom telah disesuaikan, Klik Simpan untuk menyimpan perubahan</li>
</ol>

<div id='judul_1'>Hapus Pengguna</div>
<ol class='ds'>
<li>Pilih Pengguna yang ingin Anda hapus, Anda dapat memilih lebih dari satu</li>
<li>Klik Hapus</li>
<li>Akan muncul dialog konfirmasi penghapusan</li>
<li>Klik Yes untuk menghapus, Klik No untuk pembatalan</li>
</ol>

<div id='judul_1'>Ubah Kata Sandi</div>
<ol class='ds'>
<li>Pilih salah satu Pengguna</li>
<li>Klik Ubah Kata Sandi</li>
<li>Isikan kolom Kata Sandi Baru dan Konfirmasi Kata Sandi, Kata Sandi Baru dan Konfirmasi Kata Sandi harus sama !</li>
<li>Klik Simpan untuk menyimpan perubahan, klik Batal untuk pembatalan</li>
</ol>

<div id='judul_1'>Akses Menu</div>
<ol class='ds'>
<li>Pilih salah satu Pengguna</li>
<li>Klik Akses Menu</li>
<li>Berikan tanda checklist pada menu yang dizinkan untuk diakses</li>
<li>Tutup Dialog Akses Menu</li>
</ol>

